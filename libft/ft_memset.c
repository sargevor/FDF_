#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*d;
	size_t	i;

	d = (char *) b;
	i = 0;
	while (i < len)
	{
		d[i] = c;
		i++;
	}
	return (d);
}
