#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c % 256 == 0)
		return ((char *)(ft_strlen(s) + s));
	while (s[i] != '\0')
	{
		if (s[i] == c % 256)
			return ((char *)s + i);
		i++;
	}
	return (0);
}
